//! G.723.1 spec-table data (ITU-T Recommendation, March 1996 base).
//!
//! Numeric tables extracted from the publicly-staged G.723.1 reference
//! data tables under `docs/audio/g7231/tables/`. Per the docs README,
//! numeric tables in a normative reference are facts, not creative
//! expression (cf. *Feist v. Rural Telephone Service Co.*, 1991), and
//! are reproducible as data. None of the algorithmic source is read.
//!
//! Each table here mirrors a `tables/*.csv` file one-for-one; the CSVs
//! carry a `.meta` sidecar recording spec section, dimension, Q-format,
//! and SHA-256 of the data source. These tables are FACTUAL values from
//! the ITU-T G.723.1 normative reference; they are NOT the clean-room
//! `tables` module's internally-consistent codebooks, which remain in
//! [`crate::tables`] and back the current encoder.
//!
//! This module is a stepping stone toward bitstream interoperability with
//! a spec-table G.723.1 reference decoder. The encoder + decoder in this
//! crate continue to use [`crate::tables`] today; integration of the
//! data here is staged in follow-up rounds.
//!
//! Sections follow the ITU-T G.723.1 1996 base edition.

